/**
 * 生成AI响应
 */
export function generateAIResponse(message, userInfo = {}) {
  const msg = message.toLowerCase().trim()
  
  // 问候语匹配
  if (/^(你好|您好|hi|hello|在吗|在不在)/.test(msg)) {
    return {
      text: greetings[Math.floor(Math.random() * greetings.length)],
      suggestions: ['我想贷款', '了解贷款利率', '需要准备什么材料']
    }
  }
  
  // 关键词精确匹配
  for (const [key, value] of Object.entries(keywords)) {
    if (msg.includes(key)) {
      if (commonQuestions[value]) {
        return {
          text: commonQuestions[value],
          suggestions: getSuggestions(value)
        }
      }
    }
  }
  
  // 默认回复
  return {
    text: getDefaultResponse(msg),
    suggestions: ['我想贷款', '贷款利率多少', '需要什么条件', '贷款流程是什么']
  }
}

/**
 * 获取建议问题
 */
function getSuggestions(key) {
  const suggestionsMap = {
    '利率': ['贷款条件', '贷款流程', '能贷多少额度'],
    '贷款': ['贷款利率', '贷款条件', '需要什么材料'],
    '条件': ['贷款流程', '需要什么材料', '能贷多少'],
    '流程': ['需要什么材料', '贷款利率', '贷款条件'],
    '材料': ['贷款流程', '贷款利率', '我能贷多少'],
    '额度': ['贷款利率', '贷款条件', '我适合什么产品']
  }
  return suggestionsMap[key] || ['了解更多', '贷款条件', '联系顾问']
}

